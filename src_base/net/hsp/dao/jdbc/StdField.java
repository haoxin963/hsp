	/**
	 * 格式化为标准字段名
	 * @param szKey 未必标准的名字
	 * @return 标准字段名
	 * */
	public static String fmt(Object szKey)
	{
		Object stdKey = k2k.get(toKey((String)szKey));
		if(null != stdKey) { return (String)stdKey; }
		return szKey.toString().toLowerCase();
	}
	
	/** 是否在标准字段的约束范畴之内 */
	public static boolean isConstraint(String key)
	{
		String val = k2k.get(toKey(key));
		return val != null;
	}
	
